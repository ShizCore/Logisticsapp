"""
Проверочный скрипт для системы авторизации.
Мы создадим нового пользователя, а затем попробуем с ним залогиниться.
"""
import time
from server.auth import AuthService

print("🔐 Начинаю тестирование системы авторизации...\n")

# Генерируем уникальное имя пользователя (с меткой времени),
# чтобы при повторных запусках не было ошибки "Пользователь уже существует"
unique_name = f"test_user_{int(time.time())}"
test_password = "SuperSecret123!"

# ═══════════════════════════════════════════════════════════════
# ТЕСТ 1: Хэширование пароля
# ═══════════════════════════════════════════════════════════════
print("=" * 60)
print("🧪 ТЕСТ 1: Хэширование пароля")
print("=" * 60)
hashed = AuthService.hash_password(test_password)
print(f"Обычный пароль: {test_password}")
print(f"Хэш для БД:     {hashed}")
print(f"Длина хэша: {len(hashed)} символов (bcrypt всегда выдает 60)\n")


# ═══════════════════════════════════════════════════════════════
# ТЕСТ 2: Регистрация нового пользователя в БД
# ═══════════════════════════════════════════════════════════════
print("=" * 60)
print("📝 ТЕСТ 2: Регистрация в базе данных")
print("=" * 60)
reg_result = AuthService.register(unique_name, test_password, role='manager')
print(f"Результат: {reg_result}\n")


# ═══════════════════════════════════════════════════════════════
# ТЕСТ 3: Успешный вход (Login)
# ═══════════════════════════════════════════════════════════════
print("=" * 60)
print("🔑 ТЕСТ 3: Успешный вход")
print("=" * 60)
login_result = AuthService.login(unique_name, test_password)
print(f"Результат: {login_result}\n")


# ═══════════════════════════════════════════════════════════════
# ТЕСТ 4: Вход с неверным паролем
# ═══════════════════════════════════════════════════════════════
print("=" * 60)
print("❌ ТЕСТ 4: Вход с неверным паролем")
print("=" * 60)
bad_login_result = AuthService.login(unique_name, "WrongPassword!")
print(f"Результат: {bad_login_result}\n")


# ═══════════════════════════════════════════════════════════════
# ТЕСТ 5: Попытка повторной регистрации (защита от дубликатов)
# ═══════════════════════════════════════════════════════════════
print("=" * 60)
print("🚫 ТЕСТ 5: Повторная регистрация того же логина")
print("=" * 60)
dup_result = AuthService.register(unique_name, test_password)
print(f"Результат: {dup_result}\n")

print("✅ Тестирование авторизации завершено!")